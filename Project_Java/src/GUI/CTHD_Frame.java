    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
      
        if(checkSL(soluong_txt.getText())){
            int i = jtable.getSelectedRow();
            String masp = jtable.getValueAt(i,1).toString();
            String tensp = jtable.getValueAt(i,2).toString();
            int soluong = Integer.parseInt(soluong_txt.getText());
            int sl_cu = Integer.parseInt(jtable.getValueAt(i, 3).toString());
            int gia = Integer.parseInt(jtable.getValueAt(i,4).toString());
            int tong = soluong * gia;
            CTHD cthd = new CTHD(MAHD,Integer.parseInt(masp),tensp,soluong,gia,tong);
            int tonkho = bll.getNum(masp);
            int tonkho_ht = tonkho+sl_cu;
            if(tonkho_ht<soluong)
                JOptionPane.showMessageDialog(this,"Số lượng tồn kho không đủ\nTồn kho hiện tại: "+tonkho);
            else{
                bll.UpdateTonkho(masp, tonkho_ht);
                bll.updateCTHD(cthd, jtable);
                bll.UpdateTonkho(masp, tonkho_ht-soluong);
            }
            
        }
        
    }//GEN-LAST:event_updatebtnActionPerformed

