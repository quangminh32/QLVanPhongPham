    private void delbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delbtnActionPerformed
        String masp = jtable.getValueAt(jtable.getSelectedRow(), 1).toString();
        int mahd = Integer.parseInt(jtable.getValueAt(jtable.getSelectedRow(), 0).toString());
        bll.delete(mahd,Integer.parseInt(masp));
        bll.CTHD(MAHD, jtable);
        int result = JOptionPane.showConfirmDialog(this,"Hoàn trả số lượng?","Thông Báo",JOptionPane.YES_NO_OPTION);
        if(result == JOptionPane.YES_OPTION){
            int soluong = Integer.parseInt(soluong_txt.getText());
            int tk = bll.getNum(masp);
            bll.UpdateTonkho(masp, tk+soluong);
        }
    }//GEN-LAST:event_delbtnActionPerformed

    private void insertbtnMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_insertbtnMousePressed
  
            Product sp = bll.getSP(jcombobox.getSelectedItem().toString());
            int tong = Integer.parseInt(soluong_txt.getText()) * Integer.parseInt(sp.getPrice());
            String masp = sp.getId();
            int soluong = Integer.parseInt(soluong_txt.getText());
            CTHD cthd = new CTHD(MAHD,Integer.parseInt(masp),sp.getName(),soluong,Integer.parseInt(sp.getPrice()),tong);
            if(bll.isDup(cthd)){
                JOptionPane.showMessageDialog(null,"Sản phẩm đã tồn tại");
            }
            else{
                int tk = bll.getNum(masp);
                if(tk>=soluong){
                    bll.insert(cthd,jtable);
                    bll.UpdateTonkho(masp, tk-soluong);
                }
                else{
                    JOptionPane.showMessageDialog(this,"Số lượng tồn kho không đủ\nTồn kho hiện tại: "+tk);     
                }    
            }
       
      
    }//GEN-LAST:event_insertbtnMousePressed
